//find the unique elements in an array
var unique = function(arr) {
    var uniqueArray = [];
    for (var i = 0; i < arr.length; i++) {
        if (uniqueArray.indexOf(arr[i]) == -1) {
            uniqueArray.push(arr[i]);
            }
            }
            return uniqueArray;
            };


